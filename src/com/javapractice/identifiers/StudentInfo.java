package com.javapractice.identifiers;

public class StudentInfo {
    public static void main(String[] args) {
        // Declaring variables using appropriate data types
        String studentName = "Rahul Sharma";
        int rollNumber = 102;
        byte age = 18;
        char grade = 'A';
        boolean isEnrolled = true;
        float feePaid = 15000.5f;

        // Displaying the student information
        System.out.println("Student Information:");
        System.out.println("Name       : " + studentName);
        System.out.println("Roll No.   : " + rollNumber);
        System.out.println("Age        : " + age);
        System.out.println("Grade      : " + grade);
        System.out.println("Enrolled?  : " + isEnrolled);
        System.out.println("Fee Paid   : " + feePaid);
    }
}

