package com.javapractice.supriyaAssignments.java;

public class Assignment4SumOf100Nos {
    public static void main(String[] args) {
        int sum = 0, n = 100;
        for (int i = 0; i <=n ; i++) {
            sum += i;
        }
        System.out.println("Sum of numbers from 1 till 100 :" +sum);
    }
}
