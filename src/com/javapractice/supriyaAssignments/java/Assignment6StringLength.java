package com.javapractice.supriyaAssignments.java;
/*
Write a Java program to find and print the length of a given string using the length() method.
Example:
If the string is: "Welcome to Java"
Expected Output:
The string is: Welcome to Java
Length of the string: 16
 */
public class Assignment6StringLength {
    public static void main(String[] args) {
        String welcomeString = "Welcome to Java";
        System.out.println("The string is: " +welcomeString);
        System.out.println("Length of the string : "+welcomeString.length());
    }
}
